public class Prostorija {

    String vrsta;
    int broj;
    public Prostorija(String vrsta, int broj){
        this.vrsta = vrsta;
        this.broj = broj;
    }

    public String getVrsta() {
        return vrsta;
    }

    public void setVrsta(String vrsta) {
        this.vrsta = vrsta;
    }

    public int getBroj() {
        return broj;
    }

    public void setBroj(int broj) {
        this.broj = broj;
    }


}
